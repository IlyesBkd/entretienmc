function flattenCategories(categories) {
    const flattened = [];
    Object.keys(categories).forEach(categoryName => {
        categories[categoryName].forEach(card => {
            flattened.push({
                ...card,
                category: categoryName
            });
        });
    });
    return flattened;
}

